# Lucille spent 12% of her weekly earning on DVDs and deposited the rest into her savings accounts.
# if she spent Rs42 on DVDs, how much did she deposit into her savings account?

spentOnDvd = 42
percentOfTotalEarning = 12
totalEarning = (spentOnDvd * 100) / percentOfTotalEarning
totalSaving = totalEarning - spentOnDvd

print("Deposit in Saving Account : Rs", totalSaving)