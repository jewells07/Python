# Dan rented two movies to watch last night. The first was 100 minutes long.
# The Second 110 minutes long. How many hours did it take for Dan to watch the two movies?

Movie1 = 100
Movie2 = 110
totalMin = Movie1 + Movie2
MinToHr = 60
spentTime = totalMin / MinToHr

print("Total hours to watch movies ",spentTime)